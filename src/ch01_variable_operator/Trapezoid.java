package ch01_variable_operator;

public class Trapezoid {
    public static void main(String[] args) {
        //변수 4 // int 4

        int 밑변, 윗변, 높이, 면적 ;

        밑변 = 20 ;
        윗변 = 10 ;
        높이 = 15 ;
        면적 = (밑변+윗변)*높이/2 ;

        System.out.println("밑변 : "+ 밑변);
        System.out.println("윗변 : "+ 윗변);
        System.out.println("높이 : "+ 높이);
        System.out.println("면적 : "+ 면적);

    }
}
