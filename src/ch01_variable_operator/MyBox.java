package ch01_variable_operator;

public class MyBox {
    public static void main(String[] args) {
        //4개 // 더블4
        double 너비 = 10.0 ;
        double 높이 = 5.0;
        double 넓이 = 너비*높이;
        double 둘레 = 2*(너비+높이);

        System.out.println("너비: "+너비);
        System.out.println("높이: "+높이);
        System.out.println("사각형의 넓이: "+넓이);
        System.out.println("사각형의 둘레: "+둘레);


    }
}
